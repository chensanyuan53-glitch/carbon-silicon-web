-- 7. Trigger: 评论时自动更新 comments_count 和 replies_count
CREATE OR REPLACE FUNCTION public_update_topic_comments_count()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    -- 更新话题的评论数
    UPDATE public.topics SET comments_count = comments_count + 1 WHERE id = NEW.topic_id;
    -- 如果是回复，更新父评论的回复数
    IF NEW.parent_id IS NOT NULL THEN
      UPDATE public.comments SET replies_count = replies_count + 1 WHERE id = NEW.parent_id;
    END IF;
    RETURN NEW;
  ELSIF TG_OP = 'DELETE' THEN
    -- 更新话题的评论数
    UPDATE public.topics SET comments_count = comments_count - 1 WHERE id = OLD.topic_id;
    -- 如果是回复，更新父评论的回复数
    IF OLD.parent_id IS NOT NULL THEN
      UPDATE public.comments SET replies_count = replies_count - 1 WHERE id = OLD.parent_id;
    END IF;
    RETURN OLD;
  END IF;
END;
$$;

DROP TRIGGER IF EXISTS tr_topic_comments_count ON public.comments;
CREATE TRIGGER tr_topic_comments_count
  AFTER INSERT OR DELETE ON public.comments
  FOR EACH ROW
  EXECUTE PROCEDURE public_update_topic_comments_count();

