-- ============================================================

-- 1. Create topics table (话题表)
CREATE TABLE IF NOT EXISTS public.topics (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  title         text NOT NULL,
  content       text NOT NULL,
  image_url     text,
  category      text NOT NULL,              -- 话题分类，如 '#脑洞大开', '#避坑指南'
  tag           text,                        -- 用户标签，如 '技术先锋', '行业专家'
  likes_count   integer NOT NULL DEFAULT 0,
  comments_count integer NOT NULL DEFAULT 0,
  views_count   integer NOT NULL DEFAULT 0,
  published     boolean NOT NULL DEFAULT true,
  created_at    timestamptz NOT NULL DEFAULT now(),
  updated_at    timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_topics_user_id ON public.topics(user_id);
CREATE INDEX IF NOT EXISTS idx_topics_category ON public.topics(category);
CREATE INDEX IF NOT EXISTS idx_topics_created_at ON public.topics(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_topics_published ON public.topics(published, created_at DESC);

COMMENT ON TABLE public.topics IS '碳硅广场话题表';

-- 2. Create topic_likes table (话题点赞表)
CREATE TABLE IF NOT EXISTS public.topic_likes (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  topic_id      bigint NOT NULL REFERENCES public.topics(id) ON DELETE CASCADE,
  user_id       uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  created_at    timestamptz NOT NULL DEFAULT now(),
  UNIQUE(topic_id, user_id)
);

CREATE INDEX IF NOT EXISTS idx_topic_likes_topic_id ON public.topic_likes(topic_id);
CREATE INDEX IF NOT EXISTS idx_topic_likes_user_id ON public.topic_likes(user_id);

COMMENT ON TABLE public.topic_likes IS '话题点赞表';

-- 3. Create comments table (评论表)
CREATE TABLE IF NOT EXISTS public.comments (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  topic_id      bigint NOT NULL REFERENCES public.topics(id) ON DELETE CASCADE,
  parent_id     bigint,                        -- 支持嵌套回复，NULL 表示一级评论
  user_id       uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  content       text NOT NULL,
  likes_count   integer NOT NULL DEFAULT 0,
  replies_count integer NOT NULL DEFAULT 0,   -- 回复数量
  published     boolean NOT NULL DEFAULT true,
  created_at    timestamptz NOT NULL DEFAULT now(),
  updated_at    timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_comments_topic_id ON public.comments(topic_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_comments_parent_id ON public.comments(parent_id);
CREATE INDEX IF NOT EXISTS idx_comments_user_id ON public.comments(user_id);

COMMENT ON TABLE public.comments IS '话题评论表';

-- 4. Create comment_likes table (评论点赞表)
CREATE TABLE IF NOT EXISTS public.comment_likes (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  comment_id    bigint NOT NULL REFERENCES public.comments(id) ON DELETE CASCADE,
  user_id       uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  created_at    timestamptz NOT NULL DEFAULT now(),
  UNIQUE(comment_id, user_id)
);

CREATE INDEX IF NOT EXISTS idx_comment_likes_comment_id ON public.comment_likes(comment_id);
CREATE INDEX IF NOT EXISTS idx_comment_likes_user_id ON public.comment_likes(user_id);

COMMENT ON TABLE public.comment_likes IS '评论点赞表';

-- 5. Trigger: 自动更新 topics.updated_at
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_topics_updated_at ON public.topics;
CREATE TRIGGER tr_topics_updated_at
  BEFORE UPDATE ON public.topics
  FOR EACH ROW
  EXECUTE PROCEDURE public.update_updated_at_column();

DROP TRIGGER IF EXISTS tr_comments_updated_at ON public.comments;
CREATE TRIGGER tr_comments_updated_at
  BEFORE UPDATE ON public.comments
  FOR EACH ROW
  EXECUTE PROCEDURE public.update_updated_at_column();

-- 6. Trigger: 点赞时自动更新 likes_count
-- 话题点赞
CREATE OR REPLACE FUNCTION public.update_topic_likes_count()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    UPDATE public.topics SET likes_count = likes_count + 1 WHERE id = NEW.topic_id;
    RETURN NEW;
  ELSIF TG_OP = 'DELETE' THEN
    UPDATE public.topics SET likes_count = likes_count - 1 WHERE id = OLD.topic_id;
    RETURN OLD;
  END IF;
END;
$$;

DROP TRIGGER IF EXISTS tr_topic_likes_count ON public.topic_likes;
CREATE TRIGGER tr_topic_likes_count
  AFTER INSERT OR DELETE ON public.topic_likes
  FOR EACH ROW
  EXECUTE PROCEDURE public.update_topic_likes_count();

